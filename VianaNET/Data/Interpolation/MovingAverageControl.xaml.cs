    // Eventhandler                                                              //
    ///////////////////////////////////////////////////////////////////////////////
    #region EVENTHANDLER

    private void NumberOfPointsNumeric_ValueChanged(object sender, RoutedPropertyChangedEventArgs<decimal> e)
    {
      this.Filter.NumberOfSamplesToInterpolate = (int)this.NumberOfPointsNumeric.Value;
